            <span class="cil_title">About The Cell</span>
            <span class="about_page_font">This library is a public and easily accessible resource database of images, videos, and animations of cells, capturing a wide diversity of organisms, cell types, and cellular processes. The purpose of this database is to advance research on cellular activity, with the ultimate goal of improving human health.</span>
            
            <span class="about_page_font">Cells, the building blocks of tissues, undergo dramatic dynamic rearrangements and changes in shape and motility during our lifetimes; abnormalities in these processes underlie numerous human diseases.  It is important that scientists and clinicians fully appreciate the structure and dynamic behavior of cells, including cells from diverse organisms, to make advancements to human health.</span>
            
            <span class="about_page_font">The Cell includes images acquired from historical and modern collections, publications, and by recruitment.</span>
            
            <span class="about_page_font">Contributions are welcome.  Learn more <a href="/pages/contribute">here</a>.</span>
            
        </div>
        <div class="col-md-4">
            
            <span class="cil_title">Purpose</span>
            <span class="about_page_font">This Image Library is a repository for images and movies of cells from a variety of organisms. It demonstrates cellular architecture and functions with high quality images, videos, and animations. This comprehensive and easily accessible Library is designed as a public resource first and foremost for research, and secondarily as a tool for education. The long-term goal is the construction of a library of images that will serve as primary data for research.</span>
            
            <span class="about_page_font">The Library effort represents not only the creation of the electronic infrastructure, but also a systematic protocol for acquisition, evaluation, annotation, and uploading of images, videos, and animations.</span>
