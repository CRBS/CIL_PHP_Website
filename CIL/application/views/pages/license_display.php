            <span class="cil_title">Published Images</span>
            <span class="about_page_font"><em>Please note:</em> While The Cell welcomes previously published images, if you are submitting images that have previously been published, you must have the right to distribute the image or the licensing of the publishing journal must allow for it. Please check with your publisher to determine if this is the case. Many publishers, like the ASCB, which publishes the <em>Molecular Biology of the Cell</em>, allow authors to retain copyright. However, even these publishers may limit distribution of the work. These limitations could relate to intended use (commercial and/or educational), alteration, and attribution. Publishers may also choose to use a Creative Commons license that allows for some types of re-use without permission. Authors should confirm the rights they have related to published images before selecting the appropriate option. Information on some publishers' licenses is provided here for your convenience. Please note that many journals have a waiting period before images may be used.</span>
