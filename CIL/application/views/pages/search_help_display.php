            <span class="cil_title">Basic Search</span>
            <ul>
            <li class="about_page_font">Use double quotes to restrict the search to an exact phrase, e.g. "basal body" will locate images annotated with the two word phrase "basal body" rather than the individual words basal or body</li>
            <li class="about_page_font">You can combine words or phrases by using capitalized AND, OR, NOT., e.g. basal AND body will locate images annotated with both keywords basal and body.  A search query using basal NOT body will locate images that are annotated with the keyword 'basal' but not with the word 'body'.</li>
            <li class="about_page_font">You can search for particular image if you have its CIL id.  Simply enter it in the basic search text box at the top.  e.g. CIL:9776&nbsp;&nbsp;(no spaces)</li>
            <li class="about_page_font">Bookmarking a search result will rerun that search on the current Library contents when you revisit.</li>
            <li class="about_page_font">Emailing the link of your search results to yourself and then clicking that link will rerun the search results on the current Library contents of the library.</li>
            </ul>
