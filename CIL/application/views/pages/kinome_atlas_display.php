                <span class="cil_13_no_color">
                The human kinome comprises 538 kinases playing essential cellular functions by catalyzing protein phosphorylations. Systematic annotation of subcellular spatial distribution of the kinome greatly facilitates investigation of normal and disease mechanisms. Here, we present Kinome Atlas (KA), an image-based map of the kinome annotated to 10 compartments of the cell. 456 epitope-tagged kinases, representing 85% of the human kinome, were expressed in HeLa cells and imaged by immunofluorescent microscopy under a unified condition. KA revealed kinase family-enriched subcellular localizations, and discovered a collection of new kinase localizations at mitochondria, plasma membrane, extracellular space, and other unique structures. This resource provides a snapshot of the kinome under a unified condition, and is a key step toward comprehensive understanding of the spatial and temporal dynamics of the kinome in physiological and disease states.
