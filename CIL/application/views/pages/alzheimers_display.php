            <span class="cil_title2">Summary</span>
            
            <p class="cil_description">We examined the structural organization of cytoskeletal components and membrane systems in neurons of well preserved biopsy material from Alzheimer’s disease (AD). Information was obtained from thin sectioned material using conventional electron microscopy and from thick sections with the high voltage electron microscope. Stereo viewing and computer assisted serial reconstruction techniques were employed to visualize three-dimensional relationships among cytoplasmic components of cortical neurons.</p>
            <br/>
            <p class="cil_description">We observed several interesting ultrastructural features in AD neurons. These include associations of paired helical filaments (PHF) with the membranes of the nuclear envelope and with ribosomes, differences between the distribution of the Golgi apparatus in neurons containing paired helical filament bundles as compared to neurons without these filaments, and abnormalities in the microtubules of neuronal processes in the vicinity of neuritic plaques.</p>
