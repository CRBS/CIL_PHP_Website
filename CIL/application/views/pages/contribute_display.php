            <p class="faq_regular_font">We invite you to share your images and videos in this  library.  Uploading images is easy and will only take a few minutes of your time. We are accumulating images of  all cell types from all organisms, including intracellular structures and  movies or animations demonstrating functions.   This ambitious project obviously is multiyear and relies upon the cell  biology community to populate the library.   <em>The Cell Image Library™</em> aims to be as useful to the researcher and  the public as are the sequence databases for nucleic acids and proteins.  Toward that end, the images and videos will  be annotated by professionals with broad disciplinary expertise.  Raw data as well as data with limited  processing will be the most valuable.</p>
            <p>&nbsp;</p>
            <p class="faq_regular_font top-buffer">Please join us in this effort by allowing us to include your  image collections and videos in this open access library, <em>and</em> by sharing your raw data.  Our team of professional annotators will work  with your valuable data to assure its accuracy before presenting it to the  scientific and lay communities. Their annotations will provide useful information  about the images to allow researchers to explore data generated by biologists.</p>
            <p>&nbsp;</p>
            <p class="faq_regular_font top-buffer">If you wish to upload a very large number of images, or have trouble uploading, please e-mail David Orloff at <a href="mailto: dorloff@ncmir.ucsd.edu">dorloff@ncmir.ucsd.edu</a> for assistance.</p>
