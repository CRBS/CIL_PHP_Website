            <span class="cil_title">Describing Your Images</span>
            <p class="faq_regular_font">To be searchable, each image will be entered into the library database with specific information that allows its identification during a user’s search. To aid in the annotation process, we ask that you supply us with as much information as you can about your images, including:</p>
            <p>&nbsp;</p>
            <ol class="participate_image_info">
            <li class="faq_regular_font">Biological Source (organism, cell type, cellular components, and fixation method, sectioning, etc. if applicable)</li>
            <li class="faq_regular_font">Biological Context and Probe (e.g., mitotic spindle during division labeled with tubulin primary antibody and Alexa 488 secondary or EGFP-tubulin)</li>
            <li class="faq_regular_font">Equipment used (e.g., microscope manufacturer, objective with NA, camera, confocal)</li>
            <li class="faq_regular_font">Acquisition parameters for time series (e.g., taken at 2 frames per second or confocal z stack taken with 1 Airy unit slices), post-processing of still images, if any</li>
            <li class="faq_regular_font">Magnification of image (either a scale bar or a note with microns/pixel)</li>
            </ol>
            <p class="faq_regular_font">Please include your contact information to allow the annotators to get in touch with you if they have any further questions about the image. This contact information will not be made public.</p>
